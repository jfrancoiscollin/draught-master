async def init_db() -> None:
    async with aiosqlite.connect(DB_PATH) as db:
        await db.execute("""
            CREATE TABLE IF NOT EXISTS games (
                id TEXT PRIMARY KEY,
                date TEXT NOT NULL,
                white_player TEXT DEFAULT 'Joueur',
                black_player TEXT DEFAULT 'IA',
                result TEXT,
                pdn TEXT,
                fen_positions TEXT,
                move_count INTEGER DEFAULT 0
            )
        """)
        await db.execute("""
            CREATE TABLE IF NOT EXISTS exercises (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                name TEXT NOT NULL,
                description TEXT,
                initial_fen TEXT NOT NULL,
                solution_moves TEXT NOT NULL,
                difficulty INTEGER DEFAULT 1,
                category TEXT DEFAULT 'general',
                hint TEXT
            )
        """)
        await db.execute("""
            CREATE TABLE IF NOT EXISTS user_progress (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                exercise_id INTEGER NOT NULL,
                attempts INTEGER DEFAULT 0,
                solved INTEGER DEFAULT 0,
                date_solved TEXT,
                FOREIGN KEY (exercise_id) REFERENCES exercises(id)
            )
        """)
        await db.commit()

        # Always upsert exercises with fixed IDs so Railway's persistent DB
        # picks up corrected FEN/solution data on each redeploy.
        for idx, ex in enumerate(INITIAL_EXERCISES, start=1):
            await db.execute(
                """
                INSERT INTO exercises (id, name, description, initial_fen, solution_moves, difficulty, category, hint)
                VALUES (?, ?, ?, ?, ?, ?, ?, ?)
                ON CONFLICT(id) DO UPDATE SET
                    name=excluded.name,
                    description=excluded.description,
                    initial_fen=excluded.initial_fen,
                    solution_moves=excluded.solution_moves,
                    difficulty=excluded.difficulty,
                    category=excluded.category,
                    hint=excluded.hint
                """,
                (
                    idx,
                    ex["name"],
                    ex["description"],
                    ex["initial_fen"],
                    json.dumps(ex["solution_moves"]),
                    ex["difficulty"],
                    ex["category"],
                    ex["hint"],
                ),
            )
        await db.commit()


async def save_game(
    game_id: str,
    date: str,
    white_player: str,
    black_player: str,
    result: Optional[str],
    pdn: str,
    fen_positions: List[str],
    move_count: int,
) -> None:
    async with aiosqlite.connect(DB_PATH) as db:
        await db.execute(
            """
            INSERT OR REPLACE INTO games
            (id, date, white_player, black_player, result, pdn, fen_positions, move_count)
            VALUES (?, ?, ?, ?, ?, ?, ?, ?)
            """,
            (game_id, date, white_player, black_player, result, pdn, json.dumps(fen_positions), move_count),
        )
        await db.commit()


async def get_games(limit: int = 20, offset: int = 0) -> List[Dict[str, Any]]:
    async with aiosqlite.connect(DB_PATH) as db:
        db.row_factory = aiosqlite.Row
        cursor = await db.execute(
            "SELECT * FROM games ORDER BY date DESC LIMIT ? OFFSET ?",
            (limit, offset),
        )
        rows = await cursor.fetchall()
        return [dict(row) for row in rows]


async def get_game(game_id: str) -> Optional[Dict[str, Any]]:
    async with aiosqlite.connect(DB_PATH) as db:
        db.row_factory = aiosqlite.Row
        cursor = await db.execute("SELECT * FROM games WHERE id = ?", (game_id,))
        row = await cursor.fetchone()
        if row:
            data = dict(row)
            data["fen_positions"] = json.loads(data["fen_positions"])
            return data
        return None


async def get_exercises(
    category: Optional[str] = None,
    difficulty: Optional[int] = None,
) -> List[Dict[str, Any]]:
    async with aiosqlite.connect(DB_PATH) as db:
        db.row_factory = aiosqlite.Row
        query = "SELECT * FROM exercises WHERE 1=1"
        params: List[Any] = []
        if category:
            query += " AND category = ?"
            params.append(category)
        if difficulty is not None:
            query += " AND difficulty = ?"
            params.append(difficulty)
        query += " ORDER BY difficulty ASC, id ASC"
        cursor = await db.execute(query, params)
        rows = await cursor.fetchall()
        result = []
        for row in rows:
            data = dict(row)
            data["solution_moves"] = json.loads(data["solution_moves"])
            result.append(data)
        return result


